// ËßÑËåÉÂåñ locale
function normalizeLocale(locale) {
  if (!locale) return 'en';
  
  // Êò†Â∞ÑÂà∞ Strapi API Ê†ºÂºè
  const localeMapping = {
    'en': 'en',
    'zh-hant': 'zh-Hant-HK', // ÂâçÁ´Ø‰ΩøÁî® zh-hantÔºåAPI ‰ΩøÁî® zh-Hant-HK
    'zh-hant-hk': 'zh-Hant-HK', // ÂÖºÂÆπËàäÊ†ºÂºè
    'zhhkhk': 'zh-Hant-HK', // ÂÖºÂÆπËàäÊ†ºÂºè
    'zh': 'zh-Hant-HK', // ÈªòË™ç‰∏≠ÊñáÁÇ∫È¶ôÊ∏ØÁπÅÈ´î
    'hk': 'zh-Hant-HK',
    'tc': 'zh-Hant-HK'
  };
  
  const normalized = locale.toLowerCase();
  return localeMapping[normalized] || 'en';
}

// ‰ªéË∑ØÂæÑÊé®Êñ≠È°µÈù¢ÂêçÁß∞
function inferPageNameFromPath(pathname) {
  const cleanPath = pathname.replace(/^\/[a-z]{2}(-[A-Z]{2})?\//, '/').replace(/^\//, '');
  
  const pathMapping = {
    '': 'homepage',
    'aboutus': 'aboutus',
    'contact-us': 'contact-us',
    'news': 'news',
    'services': 'services',
    'digital-solutions': 'digital-solutions'
  };
  
  if (cleanPath.startsWith('news/') && cleanPath.length > 5) {
    return 'news-detail';
  }
  
  return pathMapping[cleanPath] || 'homepage';
}

// ‰ªéÂØåÊñáÊú¨ÂÜÖÂÆπ‰∏≠ÊèêÂèñÁ∫ØÊñáÊú¨
function extractContentFromRichText(contentArray) {
  if (!Array.isArray(contentArray)) return '';
  
  return contentArray.map(block => {
    if (block.type === 'paragraph' && block.children) {
      return block.children
        .filter(child => child.type === 'text')
        .map(child => child.text)
        .join('');
    }
    return '';
  }).join(' ').trim();
}

