// Ëé∑ÂèñÈ°µÈù¢ÊâÄÈúÄÁöÑÊâÄÊúâÊï∞ÊçÆ
export async function getPageData(pageName, locale = 'en', options = {}) {
  const normalizedLocale = normalizeLocale(locale);
  const apis = PAGE_API_CONFIG[pageName] || ['menus'];
  
  // console.log(`üéØ Loading ${pageName} data for ${normalizedLocale}`);

  try {
    // Âπ∂Ë°åËé∑ÂèñÊâÄÊúâÈúÄË¶ÅÁöÑ API Êï∞ÊçÆ
    const results = await Promise.allSettled(
      apis.map(async (apiName) => {
        const config = API_ENDPOINTS[apiName];
        if (!config) {
          throw new Error(`API config not found: ${apiName}`);
        }

        const data = await fetchStrapiData(config.endpoint, {
          pLevel: config.populateLevel,
          locale: normalizedLocale,
          populate: '*',
          ...options
        });

        return { apiName, data };
      })
    );

    // ÁªÑÁªáËøîÂõûÊï∞ÊçÆ
    const pageData = {};
    const errors = [];

    results.forEach((result, index) => {
      const apiName = apis[index];
      
      if (result.status === 'fulfilled') {
        pageData[result.value.apiName] = result.value.data;
      } else {
        // console.warn(`‚ö†Ô∏è Failed to load ${apiName}:`, result.reason.message);
        errors.push({ apiName, error: result.reason.message });
        pageData[apiName] = null;
      }
    });

    // Ê∑ªÂä†ÂÖÉÊï∞ÊçÆ
    pageData.meta = {
      pageName,
      locale: normalizedLocale,
      timestamp: new Date().toISOString(),
      apis: apis,
      errors: errors,
      hasErrors: errors.length > 0
    };

    return pageData;

  } catch (error) {
    // console.error(`‚ùå Error loading ${pageName} data:`, error);
    throw error;
  }
}

