// ÈÄöÁî® Strapi Êï∞ÊçÆËé∑ÂèñÔºàÊó†ÁºìÂ≠òÔºâ
export async function fetchStrapiData(endpoint, options = {}) {
  const {
    slug = null,
    pLevel = 3,
    filters = {},
    populate = "*",
    fallbackData = null,
    draft = false,
    ...extraParams
  } = options;

  const startTime = Date.now();

  try {
    // ÊûÑÂª∫Êü•ËØ¢ÂèÇÊï∞ - Ê†πÊçÆ draft ÂèÇÊï∞ÂÜ≥ÂÆö‰ΩøÁî® live ËøòÊòØ preview Áä∂ÊÄÅ
    const queryConfig = {
      pLevel,
      populate,
      publicationState: draft ? 'preview' : 'live',
      ...extraParams,
    };

    if (slug) {
      queryConfig.filters = {
        slug: { $eq: slug },
        ...filters,
      };
    } else if (Object.keys(filters).length > 0) {
      queryConfig.filters = filters;
    }

    const queryString = qs.stringify(queryConfig);
    const url = getStrapiURL(`/api/${endpoint}?${queryString}`);

    // ÂáÜÂ§áËØ∑Ê±ÇÂ§¥
    const headers = {
      'Content-Type': 'application/json',
      'Accept': 'application/json',
    };

    // Âè™ÊúâÂú®ÊúâÊïàÁöÑ API token Â≠òÂú®Êó∂ÊâçÊ∑ªÂä† Authorization header
    if (process.env.STRAPI_API_TOKEN && 
        process.env.STRAPI_API_TOKEN !== 'your_strapi_api_token_here' &&
        process.env.STRAPI_API_TOKEN.trim() !== '' &&
        process.env.STRAPI_API_TOKEN !== 'undefined') {
      headers['Authorization'] = `Bearer ${process.env.STRAPI_API_TOKEN}`;
    }

    // Êó†ÁºìÂ≠òËØ∑Ê±Ç
    const response = await fetch(url, {
      method: 'GET',
      headers,
      cache: 'no-store', // Á¶ÅÁî®ÁºìÂ≠ò
    });

    if (!response.ok) {
      let errorMessage = `Failed to fetch ${endpoint}: ${response.status}`;
      
      switch (response.status) {
        case 401:
          errorMessage += ` (Unauthorized)`;
          break;
        case 403:
          errorMessage += ` (Forbidden)`;
          break;
        case 404:
          errorMessage += ` (Not Found)`;
          break;
        case 500:
          errorMessage += ` (Server Error)`;
          break;
      }

      throw new Error(errorMessage);
    }

    const data = await response.json();
    const duration = Date.now() - startTime;
    // console.log(`‚ö° ${endpoint} fetched in ${duration}ms`);

    return data;
    
  } catch (error) {
    // console.error(`‚ùå Error fetching ${endpoint}:`, error.message);
    
    if (fallbackData !== null) {
      // console.log(`üîÑ Using fallback data for ${endpoint}`);
      return fallbackData;
    }
    
    throw error;
  }
}

