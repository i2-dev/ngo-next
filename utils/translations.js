// 獲取翻譯文字的輔助函數
export function getTranslation(locale, category, key, fallback = '') {
  const normalizedLocale = normalizeLocale(locale);
  
  // 嘗試獲取翻譯
  const translation = translations[category]?.[normalizedLocale]?.[key];
  
  // 如果找不到翻譯，嘗試使用英文作為後備
  if (!translation && normalizedLocale !== 'en') {
    const fallbackTranslation = translations[category]?.['en']?.[key];
    if (fallbackTranslation) {
      return fallbackTranslation;
    }
  }
  
  // 如果還是找不到，返回提供的後備文字或空字符串
  return translation || fallback || '';
}

// 標準化語言代碼
function normalizeLocale(locale) {
  if (!locale) return 'en';
  
  // 處理不同的中文語言代碼變體
  if (locale === 'zh' || locale === 'zh-Hant' || locale === 'zh-hant') {
    return 'zh-hant';
  }
  
  return locale.toLowerCase();
}

// 獲取完整年份範圍的版權文字
export function getCopyrightText(locale, companyName = 'I2 COMPANY LIMITED', startYear = 2006) {
  const currentYear = new Date().getFullYear();
  const copyrightText = getTranslation(locale, 'common', 'copyright');
  
  return `${companyName} ${copyrightText} © ${startYear}-${currentYear}`;
}

